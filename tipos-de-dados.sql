-- TIPOS DE DADOS

-- NUMÉRICO
-- Inteiro: TinyInt, SmallInt, Int, MediumInt, BigInt → números inteiros com tamanhos diferentes
-- Real: Decimal, Float, Double, Real → números com casas decimais
-- Lógico: Bit, Boolean → verdadeiro/falso (0 ou 1)

-- DATA/TEMPO
-- Date: data (AAAA-MM-DD)
-- DateTime: data e hora (AAAA-MM-DD HH:MM:SS)
-- TimeStamp: data e hora atualizável
-- Time: hora (HH:MM:SS)
-- Year: ano (formato YYYY)

-- LITERAL (TEXTOS, STRINGS)
-- Caractere: Char, VarChar → texto curto (fixo ou variável)
-- Texto: TinyText, Text, MediumText, LongText → textos maiores
-- Binário: TinyBlob, Blob, MediumBlob, LongBlob → arquivos, imagens, dados binários
-- Coleção: Enum, Set → lista de valores fixos para selecionar

-- ESPACIAL
-- Geometry, Point, Polygon, MultiPolygon → dados geográficos
