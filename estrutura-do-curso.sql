-- DDL (Data Definition Language) - Comandos de definição

-- CREATE DATABASE, CREATE TABLE, ALTER TABLE, DROP TABLE

-- DML (Data Manipulation Language) - Comandos de manipulação

-- INSERT INTO, UPDATE, DELETE, TRUNCATE

-- Diferença entre DROP e TRUNCATE:

-- DROP TABLE apaga todos os dados e a estrutura da tabela.

-- TRUNCATE apaga apenas os dados, mantendo a estrutura.
